#절댓갑 abs
print("abs(-4):", abs(-4))

#all, 요소가 전부 참이면 True 하나라도 틀리면 False
print("all([1, 2, 3, 4, 0]):", all([1, 2, 3, 4, 0]))

#any 하나라도 참이면 True, 다 틀리면 False
print("any([1, 2, 3, 4, 0]):", any([1, 2, 3, 4, 0]))

#chr 유니코드 -> 문자
print("chr(97):", chr(97))

#dir 객체가 지닌 변수나 함수를 보여준다.
print("dir([1,2,3]):", dir([1,2,3]))
print("dir({'1':'a'}:", dir({'1':'a'}))